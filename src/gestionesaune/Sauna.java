package gestionesaune;

public class Sauna {
	private int numeroUnivoco;
	private int capienza;
	
	public Sauna(int numeroUnivoco, int capienza){
		numeroUnivoco = this.numeroUnivoco;
		capienza = this.capienza;
	}

	public int getNumeroUnivoco() {
		return numeroUnivoco;
	}

	public int getCapienza() {
		return capienza;
	}
}
